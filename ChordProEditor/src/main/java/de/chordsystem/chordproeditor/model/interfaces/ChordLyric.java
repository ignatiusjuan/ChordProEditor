package de.chordsystem.chordproeditor.model.interfaces;

public interface ChordLyric {

	/**
	 * @return the chord
	 */
	String getChord();

	/**
	 * @param chord the chord to set
	 */
	void setChord(String chord);

	/**
	 * @return the lyric
	 */
	String getLyric();

	/**
	 * @param lyric the lyric to set
	 */
	void setLyric(String lyric);

}